#include<iostream>

using namespace std;

main()
{
	
	char alpha=0;
	
	cout<<"Please Enter a alphabet A-E";
	cin>>alpha;
	
	if(alpha=='A')
	{
		cout<<"You Have Pressed A";
		
	}
	
	else if(alpha=='B')
	{
		cout<<"You Have Pressed B";
		
	}
	
	else if(alpha=='C')
	{
		cout<<"You Have Pressed C";
		
	}
	
	else if(alpha=='D')
	{
		cout<<"You Have Pressed D";
		
	}
	
	else if(alpha=='E')
	{
		cout<<"You Have Pressed E";
		
	}
	
		
	else
	{
		cout<<"You Have Pressed value other than A-D";
		
	}
	
}


