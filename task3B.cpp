#include<iostream>

using namespace std;

main()
{
	
	char alpha=0;
	
	cout<<"Please Enter a alphabet A-E";
	cin>>alpha;
	
	if(alpha=='A'||alpha=='a')
	{
		cout<<"You Have Pressed A";
		
	}
	
	else if(alpha=='B'||alpha=='b')
	{
		cout<<"You Have Pressed B";
		
	}
	
	else if(alpha=='C'||alpha=='c')
	{
		cout<<"You Have Pressed C";
		
	}
	
	else if(alpha=='D'||alpha=='d')
	{
		cout<<"You Have Pressed D";
		
	}
	
	else if(alpha=='E'||alpha=='e')
	{
		cout<<"You Have Pressed E";
		
	}
	
		
	else
	{
		cout<<"You Have Pressed value other than A-E";
		
	}
	
}


